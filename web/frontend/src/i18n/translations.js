/**
 * Get translation for a key in the specified language
 * @param {string} lang - Language code (zh-tw, zh-cn, en, ja)
 * @param {string} key - Translation key
 * @returns {string} Translated text or the key if not found
 */
export function t(lang, key) {
  const langTranslations = translations[lang] || translations['zh-tw'];
  return langTranslations[key] || key;
}

/**
 * Get all translations for a language
 * @param {string} lang - Language code
 * @returns {object} All translations for the language
 */
export function getTranslations(lang) {
  return translations[lang] || translations['zh-tw'];
}

export default translations;
